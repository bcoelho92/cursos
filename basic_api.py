# Calculadora Básica: Um programa que pode realizar operações matemáticas básicas como adição,
# subtração, multiplicação e divisão.

# Gerador de Senhas: Um script que gera senhas aleatórias com critérios de segurança,
# como uso de letras maiúsculas, minúsculas, números e caracteres especiais.

# Jogo da Forca: Um pequeno jogo onde o usuário deve adivinhar a palavra antes de esgotar as tentativas.

# Lista de Tarefas: Uma aplicação simples para adicionar, remover e visualizar tarefas pendentes.

# Conversor de Unidades: Um programa que converte valores entre diferentes unidades de medida,
# como temperatura (Celsius para Fahrenheit), distância (quilômetros para milhas), etc.